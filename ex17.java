
// exemplo de for
// 1. Laço for
// O laço for é usado quando você sabe quantas vezes deseja iterar. É comum em situações onde você precisa percorrer um array ou uma lista.
public class ExemploFor {
    public static void main(String[] args) {
        System.out.println("Contagem de 1 a 5 usando for:");
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }
}
